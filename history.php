	* 1970 - For the first time participates in an inter-school event.<br>
	* 1971 - First batch of 18 students appears for SSC board exam.
                 100% results!<br>
	* 1972 - Revised pattern of "Ten year SSC Course" introduced.<br>
	* 1973 - New Basketball court inaugurated.<br>
	* 1975 - Both Xth (New) & XIth (Old) Stds. appear for SSC exam.<br>
	* 1976 - New School building inaugurated on March 2nd.<br>
	* 1977 - During the academic year several new schemes
             introduced:<br>
            Group system with more scope for inter-action is  followedfor teaching of Value Education.<br>
            Work experience classes (Cookery & Knitting).<br>
            Open Day dialogue between Parents & Teachers regarding Children's performance.<br>
            Structured functioning of House system. News for the   day.<br>
